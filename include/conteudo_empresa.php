      <p>A Full Screen foi criada em 1996 para atuar no desenvolvimento de soluções corporativas para empresas que buscam aprimorar o controle interno apoiado em sistema de gestão administrativa com módulos integrados em todos os setores da empresa no padrão (ERP).</p>

      <p>Nosso principal foco são as empresas do comércio, as indústrias, metalúrgicas entre outras. Nossos sistemas são parametrizados e atendem também aos segmentos de Atacado/Distribuição, Auto-Peças, pequenos comércios até grandes lojas de departamentos.</p>

